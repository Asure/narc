* CANAWAY - MAKE A BACK/FOREGROUND OBJECT GET HURLED FROM AN EXPLOSION	 *
* A8 = PTR TO BACK/FOREGROUND OBJECT					 *
* A10 = PTR TO THE 'TING THAT SMASHED IT MAAHN				 *
