*
*CLOWN DISPATCHER
*
*CLWNDISP:
*	CREATE	0CDH,CLWAIT
*	RETS
*CLWAIT:
*	SLEEP	30
*	CREATE	0E1H,CLNSND
*CLWNDIS1:
*	MOVE	@KNAPCNT,A0,W		;HOW MANY CLWN BABIES?
*	CMPI	1,A0
*	JRHS	CLWNDIS4		;DON'T NEED MORE
*	MOVI	00E60H,A14
*	CLR	A5
*	CALLR	CLWNCOLD
*CLWNDIS4:
*	SLOOP	30,CLWNDIS1		;WAIT FOR THEM TO DIE
*
*CLOWN ENTRY SOUND PROCESS
*
CLNSNDST
	CREATE	INDPID,CLNSND
	RETS
CLNSND:
	SLEEP	4			;WAIT FOR CLOWNS TO COME OUT
	MOVE	@KNAPCNT,A0,W
	JREQ	CLNSND

	MOVI	CLTUNSND,A0,L		;PLAY CLOWN TUNE... THEIR COMING
	CALLA	ONESND
       	SLEEP	300
 	MOVI	CLAFSND,A0,L
	CALLA	ONESND
	JAUC	SUCIDE
*
*CLOWN COLD START
*A5=Y:X COORDINATE
*A14=SUBTYPE B0-3
CLWNCOLD:
	MOVE	A14,A10
	MOVE	A5,A9
	CREATE	00E60H,CLOWN	
	MOVE	A10,A1
	SLL	28,A1
	SRL	22,A1
	ADDI	CLNPTAB,A1
	ADDI	ETEMP4,A0  	;LOAD ETEMP4,5,6
	MOVE	*A1+,*A0+,W
	MOVE	*A1+,*A0+,W
	MOVE	*A1,*A0,W
	RETS
*
*CLOWN PROCESS
*A9=XY COORD
*A10= OID
*ETEMP4=JUMP PROB
*ETEMP4+010H=GIRL PROB
*ETEMP5=GIVEUP PROB
*
CLOWN:
	MOVE	@KNAPCNT,A0,W		;HOW MANY CLOWNS?
	MOVE	@CLWNMAX,A1,W
	CMP	A0,A1
	JALS	SUCIDE			;DON'T NEED MORE

	MOVE	A9,A9  			;PRESET COORD?
	JRNE	CLWN2XX			;YES, USE IT

	MOVE	@WORLDTL,A1,W		;COME FROM EDGE OF SCREEN
	CALLA	RANDOM
	MOVE	A0,A0
	JRN	CLWN2X 		
	ADDI	512,A1			;START HIM AT RIGHT SCREEN EDGE
CLWN2X:
	ZEXT	A1

	MOVE	@PLYMIN,B0,W
	MOVE	@PLYMAX,B1,W
	CALLA	RANGRAND
	SUBI	74,A0
	SLL	16,A0
	ADD	A0,A1
	MOVE	A1,A9
CLWN2XX:
	MOVE	*A13(ETEMP4),A0,W	;GET JUMP PROB
	CALLA	RANDPER
	JRNC	CLWN1X	     
	JSRP	JUMPER			;LETS DO A JUMPER
	CMPI	P1DATA,A11
	JREQ	GPLAYR			;WERE AFTER PLAYER
	CMPI	P2DATA,A11
	JREQ	GPLAYR
	JRUC 	GHOOKR			;WERE AFTER HOOKERS
CLWN1X:
	MOVE	A9,A1		;GET Y:X COORD
	MOVI	[1,1],A5	;SET SPEED
	MOVI	CLWNTAB,A6
	MOVI	CLWNINIT,A14	;CLOWN ENEMY INIT
	MMTM	SP,A10
	CALLA	ESINITUL	;CREATE CLOWN
	MMFM	SP,A10
	JAEQ	SUCIDE		;CREATE ERROR, EXIT
	CALLR	CLNSETUP
	JRUC	CLOWNST
*
*CLOWN PROBABILITY TABLE
*
JMPROB 	.SET	000H	;JUMP PROB		
GRLPROB	.SET	010H	;GET GIRL PROB
QTPROB	.SET	020H	;QUIT PROB
CLNPTAB:
	.WORD	500,750,750,0		;NORMAL DUDE
	.WORD	500,700,000,0		;DOESN'T GIVE UP
	.WORD	500,1000,750,0		;GIRL GETTER
	.WORD	1000,700,750,0		;JUMPER
*
*CLOWN SETUP 
*A8=OBJECT
*A10=OID
*A13=PROCESS
*
CLNSETUP:
	MMTM	SP,A0,A1		;PUSH REGGIES
	MOVE	A10,*A8(OID),W		;SAVE OBJECT ID
	CLR	A0
	MOVE	A0,*A13(ETEMP2),L	;NO GIRL YET...
	MOVE	@CLWNSHT,A0,W		;SHOTS TO TAKE
	MOVE	A0,A1
	CALLA	RANDU
	ADD	A1,A0
	MOVE	A0,*A13(ETEMP3),W	;SETUP SHOT COUNT

	MOVE	@KNAPCNT,A0,W
	INC	A0
	MOVE	A0,@KNAPCNT,W 	;INCREMENT CLOWN COUNT
	MMFM	SP,A0,A1
	RETS

*
*ETEMP2=GOT HOOKER FLAG
*ETEMP3=SHOT COUNT
*ETEMP4=JUMPER PROB
*ETEMP4+010H=GIRL PROB
*ETEMP5=GIVEUP PROB
CLOWNST:
	CLR	A0
	MOVE	A0,*A13(ETEMP2),L	;NO GIRL YET...
	MOVE	*A13(ETEMP4+010H),A0,W	;GET HOOKER %
	CALLA	RANDPER
	JRC	GHOOKR
*
*GET NEAREST PLAYER AND STAB HIM
*A8=CLOWN OBJECT
*
GPLAYR:
	MOVI	CLWNWLK,A9
	CALLA	RANDOM
	JRN	GPLAYR1
	MOVI	CLWNRUN,A9
GPLAYR1:
	CALLA	GETCHOK	       ;IF CLOSE HOOKER, BAG HER
	JRNE	GHOOKR
	CMPI	CLWNRE,A9	
	JRHS	GPLAYR		;INVALID ANIMATION, RESET...
	MOVK	4,A1
	JSRP	FRANIM
	MOVE	A0,A10		;SAVE SLEEP TIME
	JRC	GPLAYR
GPLAYRL:
	CALLA	GETCPLY		;GET CLOSEST PLAYER
	MOVE	*A0(PFLAGS),A7,L
	MOVI	25,A5
	MOVI	15,A6
	MOVE	*A0(POBJ),A0,L 	;PLAYER OBJECT
	BTST	BDEAD,A7	;PLAYER DOWN?
	JREQ	GPLYRL1		;NOPE
	MOVI	7,A6
	CLR	A1		;NO X OFFSET
	MOVI	-5,A3
	CALLA	XZCNTCK		;CENTERED XZ CHECK
	JRUC	GPLYRL2
GPLYRL1:
	CALLA	XZCHK		;ARE WE IN POSITION
GPLYRL2:
	JRNC	GPLAYR2		;NO, GET DIRECTION
GPLAYRL0
	BTST	BJUMP2,A7	;IN AIR ?
	JRNE	GPLAYR20	;YES, BE STUPID

	CMPI	15,A3
	JRHS	CLSTAB		;NOT TOO CLOSE
	MOVE	*A8(OXVEL),A0,L	;NO X VEL ?
	JREQ	GPLAYR2		;GIVE HIM XVEL
	CLR	A0
	MOVE	A0,*A8(OZVEL),L	;COOL VELOCITY
	MOVE	A0,*A8(OYVEL),L	;COOL VELOCITY
	CALLA	SHVELCPY  	;DO IT FOR SHADOW
	JRUC	GPLAYR20
GPLAYR2:
	CMPI	80,A3		;CLOSE ENOUGH TO JUMP?
	JRHS	GPLAYR3
	CMPI	80,A4
	JRHS	GPLAYR3
	MOVI	10,A0
	CALLA	RANDPER
	JRNC	GPLAYR3
	JSRP	JUMPLIL		;DO A LITTLE JUMP
	JRUC	GPLAYRL
GPLAYR3:
	CALLR	CLWNDIR		;NO GET DIRECTION
GPLAYR20:
	SLEEP	1
	DSJ	A10,GPLAYRL
	JRUC	GPLAYR1
*
*XZCNTCK
*X CHECK IS ON OBJECT CENTERS NOT ANIMATION POINT
*CALLING PARAMETERS:
*A0=OBJECT SEEKED
*A1= X OFFSET FROM SEEKED OBJECT
*A3= Z OFFSET
*A5=X RANGE
*A6=Z RANGE
*A8=OBJECT SEEKING
*RETURNS:
*CARRY SET IF HE IS CLOSE ENOUGH
*A1=DELTA X
*A2=DELTA Z
*A3=ABS DELTA X
*A4=ABS	DELTA Z
XZCNTCK:
	MOVE	*A0(OSIZEX),A2,W
	SRL	1,A2
	ADD	A1,A2
	MOVE	*A0(OXPOS),A1,W
	ADD	A2,A1
	MOVE	*A0(OZPOS),A2,W
	ADD	A3,A2
	MOVE	*A8(OSIZEX),A4,W
	SRL	1,A4
	MOVE	*A8(OXPOS),A3,W
	ADD	A4,A3
	MOVE	*A8(OZPOS),A4,W	       
	SUB	A4,A2
	SUB	A3,A1
	MOVE	A1,A3
	MOVE	A2,A4
	ABS	A3
	ABS	A4
	CMP	A5,A3
	JRNC	XZCNTX
	CMP	A6,A4
XZCNTX:
	RETS
*
*CLOWN BLOOD START
*A8=CLOWN, A0=PLAYER DATA 
*
CLBLOOD:
	MMTM	SP,A8,A11
	MOVE	*A8(OID),A11,W
	MOVE	*A8(OYPOS),A9,W
	ADDI	25,A9			;ADJUST FOR KNIFE POSITION
	MOVE	*A0(PFLAGS),A8,L
	BTST	BSWALK,A8
	JREQ	CLBLD0
	ADDI	8,A9			;LOWER BLOOD FOR SQUAT WALK
CLBLD0:
	MOVE	*A0(POBJ),A8,L
	CREATE	0EDH,SPLATBOX
	MOVI	15,A8
	MOVE	A8,*A0(PTIME),W		;DELAY START OF BLOOD
	MMFM	SP,A8,A11
	RETS
*STAB THE SUCKER, GACY BABY
CLSTAB:
	MOVE	A1,A1		;FLIP THE DUDE PROPERLY
	JRN	CLSTAB0
	CALLA	NOYFLP
	JRUC	CLSTAB1
CLSTAB0:
	CALLA	YFLP
CLSTAB1:
	CALLA	PSTOP			;STOP THE DUDE
	CALLA	GETCPLY
	MOVE	A0,A2			;GIVE PLAYER KNIFE HIT
	CALLA	KNIFEHIT
	MOVE	*A0(PFLAGS),A1,L
	BTST	BDEAD,A1		;PLAYER LYING DOWN?
	JREQ	STABHI			;NO, UPRIGHT...

*STAB HIM LOW DOWN
	MOVE	*A0(POBJ),A2,L		;FLIP THE DUDE OPPOSITE TO PLAYER
	MOVE	*A2(OFLAGS),A1,L
	BTST	B_FLIPH,A1
	JRZ	CSTBLO1
	CALLA	NOYFLP
	JRUC	CSTBLO2
CSTBLO1:
	CALLA	YFLP
CSTBLO2:
	MOVE	*A8(OSIZEY),A1,W	;ALREADY LOW?
	CMPI	50,A1
	JRLS	STABLO1			;YES...
	MOVI	CLWNKST1,A9
	MOVK	1,A1
	JSRP 	FRANIM
STABLO1:
	MOVI	CLWNKST2,A9
	MOVI	CLSTBSND,A0
	CALLA	ONESND
	MOVK	1,A1
	JSRP	FRANIM			;STAB BEFORE SPURT			
	CALLA	GETCPLY
	MOVE	*A0(PFLAGS),A1,L
	BTST	BDEAD,A1		;PLAYER LYING DOWN?
	JREQ	STABLO2			;NO, UPRIGHT NO BLOOD WE MISSED
	MOVI	0007A000CH,A9		;Y:X OFFSET
	CREATE	0EDH,SPLATS
STABLO2:
	MOVI	CLWNKST3,A9
	JRUC	CLSTAB5

*MAKE SOME BLOOD
STABHI:
	CALLR	CLBLOOD
	CMPI	CLWNSTBE,A9		;JUST FINISHED REG STAB?
	JRNE	CLSTAB2
	MOVI	500,A0
	CALLA	RANDPER			;GO FOR A REPEAT?
	JRNC	CLSTAB2			;NOPE
	MOVI 	STABREP,A2
	JRUC	CLSTAB3
CLSTAB2:
	MOVI	STABTAB,A2
	MOVI	4,A0
	CALLA	RANDU
	DEC	A0
	SLL	6,A0
	ADD	A0,A2
CLSTAB3:
	MOVE	*A2+,A9,L		;GET ANIMATION
	MOVE	*A2+,A0,L		;GET SOUND
CLSTAB4:
	CALLA	ONESND
CLSTAB5:
	MOVK	1,A1
	JSRP	FRANIM			;STAB HIM
	MOVI	800,A0
	CALLA	RANDPER
	MOVK	1,A10
	JRC	GPLAYRL			;GET HIM AGAIN

*LAUGH IT UP
LAFITUP:
	MOVI	CLAFSND,A0		;LAUGH IT UP
	CALLA	ONESND
	MOVI	CLWNLAF,A9
	MOVK	1,A1
	JSRP	FRANIM
	MOVE	*A13(ETEMP5),A0,W	;GET QUIT PROB
	CALLA	RANDPER
	JRNC	CLOWNST			;GET HIM AGAIN...

*RUN AWAY
	MOVE	@CLWNSPD,A1,W		;GET VELOCITY
	SLL	9,A1			;NORMALIZE IT
	MOVE	@RAND,A0,W
	JRN	RUN0
	NEG	A1
RUN0:
	MOVE	A1,*A8(OXVEL),L
	CALLA	SHVELCPY  		;DO IT FOR SHADOW
	MOVE	A1,A1			;TEST X VELOCITY
	JRN	RUN1
	CALLA	NOYFLP
	JRUC	RUN2
RUN1:
	CALLA	YFLP
RUN2:
	MOVI	20,A0
	CALLA	RANDU
	MOVE	A0,A10
RUNLP:
	MOVI	CLWNRUN,A9
	MOVK	1,A1
	JSRP	FRANIM
	MOVE	@WORLDTL,A0,W		;CHECK FOR FAR OFF SCREEN
	ADDI	256,A0			;NORMALIZE FOR SCREEN CENTER
	MOVE	*A8(OXPOS),A1,W	
	SUB	A0,A1
	ABS	A1
	CMPI	350,A1
	JRHS	RUNLP1			;BAG IT TOO FAR OUT
	DSJS	A10,RUNLP
	JRUC	CLOWNST
*CALL IT QUITS- TOO FAR OFF	
RUNLP1:
	MOVE	@KNAPCNT,A0,W
	DEC	A0
	MOVE	A0,@KNAPCNT,W 	;DECREMENT CLOWN COUNT
	MOVE	A8,A0
	CALLA	DELENEMY	;OFF ENEMY
	JAUC	SUCIDE		;BAG IT...
*
*STAB ACTION/SOUND TABLE
*
STABTAB:
	.LONG	CLWNSTAB,CLSTBSND	;REGULAR STAB
	.LONG	CLWNSTBF,CLSLHSND	;FOREHAND STAB
	.LONG	CLWNSTBB,CLSLHSND	;BACKHAND STAB
	.LONG	CLWNSTOH,CLSTBSND	;OVERHEAD SMASH
STABREP:
	.LONG	CLWNSTB2,CLSTBSND	;QUICKIE REGULAR STAB
*
*JUMP FROM THE GROUND
*A8=CLOWN
*A1=DX, A2=DZ
JUMPLIL:
	CALLA	SHADST
	SLL	10,A1		;GET XVEL
	MOVE	A1,*A8(OXVEL),L
	SLL	10,A2
	MOVE	A2,*A8(OZVEL),L
	ADDI	-030000H,A2
	MOVE	A2,*A8(OYVEL),L
	CALLA	SHVELFLP	;SHADOW VELOCITY AND FLIP
	JRUC	JUMPRC		;DO YOUR JUMP DUDE...
*
*JUMPING CLOWN
*
JUMPER:
*	MOVE	A9,A1		;GOT A COORD?
*	JRNE	JUMPER00	;YES...
*MAKE YOUR OWN COORDINATE
	MOVI	350,A0
	CALLA	RANDU
	ADDI	75,A0
	MOVE	@WORLDTL,A1,W
	ADD	A0,A1		;SET INITIAL X
	ZEXT	A1
	MOVE	@PLYMIN,A0 	;GET INITIAL Y
	SLL	16,A0
	ADD	A0,A1
JUMPER00:
	MOVI	[1,1],A5	;SET SPEED
	MOVI	CLWNTAB,A6
	MOVI	CLWNINIT,A14	;CLOWN ENEMY INIT
	MMTM	SP,A10,A11
	CALLA	ENINITLL	;CREATE CLOWN
	MMFM	SP,A10,A11
	JAEQ	SUCIDE		;CREATE ERROR, EXIT
	CALLR	CLNSETUP	;SETUP SOME STUFF, INC KNAPCNT ETC.

	MOVE	@PLZMIN,A1,W
	MOVE	A1,*A8(OZPOS),W
	MOVE	*A8(OSHAD),A0,L
	JREQ	JUMPER0
	CALLA	SHADCPY		;RESET SHADOW AND Z FOR PLZMIN

JUMPER0:
	MOVE	*A8(OYPOS),A0,W	;SET UP Y HEIGHT
	SUBI	100,A0
	MOVE	A0,*A8(OYPOS),W	;SET UP Y HEIGHT

	MOVE	*A13(ETEMP4+010H),A0,W	;GET HOOKER %
	CALLA	RANDPER
	JRNC	JMPLAY
	CALLA	GETHOK		;GET CLOSEST HOOKER IN A0
	JREQ	JMPLAY		;NO HOOKERS AROUND...GET THE PLAYER...
	MOVE	A0,A11		;SAVE HOOKER OBJECT
	JRUC	JMPVEL
JMPLAY:
	CALLA	SELCTPLY
	MOVE	A11,A0
	CALLA	GETPLYR
	MOVE	*A0(POBJ),A0,L	 ;GET TARGET

*COMPUTE JUMP VELOCITIES
JMPVEL:
	MOVE	*A0(OXVAL),A1,L	 ;COMPUTE X VELOCITY
	MOVE	*A8(OXVAL),A2,L
	MOVE	*A0(OXVEL),A3,L
	SUB	A2,A1
	SRA	6,A1
	ADD	A1,A3
	MOVI	0C0000H,A2    	;CHECK CLOWN VELOCITY LIMITS
	CMP	A2,A3
	JRLT	JMPV1
	MOVE	A2,A3
JMPV1
	NEG	A2
	CMP	A2,A3
	JRGT	JMPV2
	MOVE	A2,A3
JMPV2
	MOVE	A3,*A8(OXVEL),L
	CALLA	FLP		 ;FLIP

	MOVE	*A0(OZVAL),A1,L	 ;COMPUTE Z VELOCITY
	MOVE	*A8(OZVAL),A2,L
	MOVE	*A0(OZVEL),A3,L
	SUB	A2,A1
	SRA	6,A1
	ADD	A3,A1
	JRNN	JUMPER1
	CLR	A1    			;NO NEGATIVE Z VELOCITY
JUMPER1:
	MOVE	A1,*A8(OZVEL),L

	ADDI	-010000H,A1		 ;GET Y VELOCITY
	MOVE	 A1,*A8(OYVEL),L
	CALLA	SHVELCPY	;COPY VELOCITY TO SHADOW
JUMPRC:
	MOVI	CLWNJMP,A9     	;JUMP ON THE DUDE
JUMP0:
	MOVK	4,A1
	JSRP	FRANIM
	JRNC	JUMP1
	MOVI	CLWNJMP0,A9	;HOLDING FRAME
	JRUC	JUMP0
*FALLING GRAVITY PROCESS
JUMP1:
	SLEEP	3
	CALLR	ZCHK			;MAKE SURE Z IN BOUNDS
	MOVE	*A8(OYVEL),A0,L		;EFFECT OF GRAVITY
	ADDI	05000H,A0
	MOVE	A0,*A8(OYVEL),L
	JRN	JUMP0			;UPWARD VEL, NO LAND
	CALLA	DFRMGRND
	CMPI	0,A1
	JRGT	JUMP0			;IN THE AIR STILL

*	MOVI	CLWNJMP1,A9     	;LANDING FRAMES
*	MOVK	1,A1
*	JSRP	FRANIM

	CALLA	PSTOP			;STOP THE SUCKER   
	MOVE	A8,A0			;OBJECT IN A0 FOR SETZPOS
	CALLA	SETZPOS			;ADJUST THE MUTHA
	CALLA	DELSHAD			;RESET YOUR SHADOW
*	CALLA	SHADST
	RETP
*
*CHECK Z FOR BOUNDS
*A8=OBJECT
*CLEARS ZV IF OUT OF RANGE
*
ZCHK:
	MMTM	SP,A0,A1,A2,A3,A4
	MOVE	*A8(OZPOS),A4,W		;CHECK IF Z LEGAL
	MOVE	@PLZMIN,A2,W
	SUB	A2,A4			;CHECK LOWER BOUND
	JRGE	ZOK2
ZBAD:
	MOVE	*A8(OYVEL),A1,L
	MOVE	*A8(OZVEL),A3,L
	SUB	A3,A1			;REMOVE ZVEL FROM YVEL
	MOVE	*A8(OYPOS),A3,W
	SUB	A4,A3			;CORRECT OYPOS ALSO
	CLR	A0		
	MOVE	A1,*A8(OYVEL),L
	MOVE	A0,*A8(OZVEL),L	     
	MOVE	A3,*A8(OYPOS),W
	MOVE	A2,*A8(OZPOS),W
	MOVE	*A8(OSHAD),A0,L		;SET Z POSITION
	JREQ	ZOK2
	CALLA	SHADCPY			;RESET SHADOW AND Z FOR PLZMIN
ZOK2:
	MMFM	SP,A0,A1,A2,A3,A4
	RETS
*
*GO GET HOOKER
*
GHOOKR:
GHOOKR0:
	MOVI	CLWNWLK,A9
	CALLA	RANDOM
	JRN	GHOOKR1
	MOVI	CLWNRUN,A9
GHOOKR1:
	CMPI	CLWNRE,A9	
	JRHS	GHOOKR		;INVALID ANIMATION, RESET...
	MOVK	4,A1
	JSRP	FRANIM
	MOVE	A0,A10		;SAVE SLEEP TIME
	JRC	GHOOKR0
GHOOKRL:
	CALLA	GETCHOK		;GET CLOSEST HOOKER IN A0
	JRNE	GPLAYR10	;FOUND A CLOSIE
	CALLA	GETHOK		;GET CLOSEST HOOKER IN A0
	JREQ	GPLAYR		;NO HOOKERS AROUND...GET THE PLAYER...
GPLAYR10
	MOVE	A0,A11		;SAVE HOOKER OBJECT
	MOVI	25,A5
	MOVI	10,A6
	CALLA	XZCHK		;ARE WE IN POSITION
	JRNC	GHOOKR2		;NO, GET DIRECTION

	CMPI	15,A3
	JRHS	GOTHER		;NOT TOO CLOSE

	MOVE	*A8(OXVEL),A0,L	;NO X VEL ?
	JREQ	GHOOKR2		;GIVE HIM XVEL
	CLR	A0
	MOVE	A0,*A8(OZVEL),L	;COOL VELOCITY
	MOVE	A0,*A8(OYVEL),L	;COOL VELOCITY
	CALLA	SHVELCPY  	;DO IT FOR SHADOW
	JRUC	GHOOKR20
GHOOKR2:
	CMPI	80,A3		;CLOSE ENOUGH TO JUMP?
	JRHS	GHOOKR3
	CMPI	80,A4
	JRHS	GHOOKR3
	MOVI	20,A0
